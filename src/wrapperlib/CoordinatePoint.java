package wrapperlib;

/**
 * Wrapper class for an x,y coordinate.<br/>
 * Mainly used as a convenient way to return a coordinate from a method.
 * 
 * @author Jono
 *
 */

public class CoordinatePoint {
	private float x;
	private float y;
	
	public CoordinatePoint() {
		this.x = 0;
		this.y = 0;
	}
	
	public CoordinatePoint(float x, float y) {
		this.x = x;
		this.y = y;
	}
	
	public void setY(float y) {
		this.y = y;
	}
	public float getY() {
		return y;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getX() {
		return x;
	}
}
